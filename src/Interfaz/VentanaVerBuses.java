    public void colocarInformacion(String cad,String ubicacion){
        /// Colocar texto en un campo de texto
        jTextArea1.append(System.getProperty("line.separator")); // Esto para el salto de l√≠nea
        jTextArea1.setText(cad);
        jTextFieldLugarParada.setText(ubicacion);
    }
    
    public  String obtenerCodigo(){
        return  jTextField1.getText();
        
    }
    
    public void colocarMapa(String ubicacion) throws MalformedURLException, UnsupportedEncodingException{
        this.labelMapa.setText("");
        Image imagenMapa = ObjStaticMaps.getStaticMap(ubicacion,
                16, new Dimension(2300, 1800), 1, StaticMaps.Format.png,
                StaticMaps.Maptype.roadmap);
        
        if (imagenMapa != null) {
            ImageIcon imgIcon = new ImageIcon(imagenMapa);
            Icon iconImage = (Icon) imgIcon;
            labelMapa.setIcon(iconImage);
        }
    
    }
