    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       SetBuses Listabuses=null;
        try {
            Listabuses = new SetBuses();
        } catch (IOException ex) {
            Logger.getLogger(VentanaVerBuses.class.getName()).log(Level.SEVERE, null, ex);
        }
       ///  validacion que el usuario Ingrese el codigo
       if(obtenerCodigo().isEmpty()){
            JOptionPane.showMessageDialog(this, "INGRESE CODIGO DE PARADA ");
       }
       else{
            
            Listabuses.EncontrarBusesUnaParada(obtenerCodigo());
            if(Listabuses.isEncontrarBus()==true){                  /// validar si se encuentra la parada
                 try {
                colocarMapa(Listabuses.getUbicacionCrearMapa());
                colocarInformacion(Listabuses.getCadenaInformacion(),Listabuses.getUbicacionCrearMapa());
                } catch (MalformedURLException ex) {
                 Logger.getLogger(VentanaVerBuses.class.getName()).log(Level.SEVERE, null, ex);
                } catch (UnsupportedEncodingException ex) {
                 Logger.getLogger(VentanaVerBuses.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            else{
                JOptionPane.showMessageDialog(this, "PARADA INGRESADA NO EXISTE ");
            }        
       }
        
         
    }//GEN-LAST:event_jButton1ActionPerformed

